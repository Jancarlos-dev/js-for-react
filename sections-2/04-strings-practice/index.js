//usando length puedo saber la cantida de letra del string
// Tipo de dato String: se utiliza para almacenar texto.
let hades = "jancarlo garcias";

// .length
// Devuelve la cantidad de caracteres que tiene el texto.
console.log(hades.length);

// [] indexar
// Permite acceder a un carácter específico usando su posición.
// Los índices comienzan desde 0.
console.log(hades[0]);

// indexOf()
// Busca un carácter o texto dentro de un string y devuelve su posición.
// Si no lo encuentra, devuelve -1.
console.log(hades.indexOf("j"));
