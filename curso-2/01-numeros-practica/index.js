// Multiplica 50 por 2
console.log(50 * 2);

// Divide 100 entre 4
console.log(100 / 4);

// Suma 10 más 20
console.log(10 + 20);

// Resta 5 a 30
console.log(30 - 5);

// Eleva 2 a la potencia de 3 (2³)
console.log(2 ** 3);

// Obtiene el residuo de dividir 15 entre 4
console.log(15 % 4);

// Verifica si 10 es mayor que 5
console.log(10 > 5);

// Verifica si 3 es menor que 7
console.log(3 < 7);

// Verifica si 5 es estrictamente igual a 5
console.log(5 === 5);

// Verifica si 10 es diferente de 5
console.log(10 !== 5);

// Verifica si 10 es mayor o igual que 10
console.log(10 >= 10);

// Verifica si 5 es menor o igual que 3
console.log(5 <= 3);

// Devuelve true solo si ambos valores son true
console.log(true && false);

// Devuelve true si al menos uno de los valores es true
console.log(true || false);

// Invierte el valor booleano (true pasa a false)
console.log(!true);
