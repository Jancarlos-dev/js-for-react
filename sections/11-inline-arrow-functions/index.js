// Creé una Arrow Function llamada showtext.
// Esta función retorna el texto "Hello World!".
const showtext = () => {
  return "Hello World!";
};

// Ejecuté la función y mostré el resultado en consola.
console.log(showtext());

// Creé una Arrow Function usando la forma corta.
// Como solo retorna un valor, no necesito usar {} ni return.
const showtext2 = () => "hades";

// Esta Arrow Function retorna un número.
const shownumber = () => 123;

// Esta Arrow Function retorna un valor booleano.
const showboolean = () => true;

// Esta Arrow Function retorna un arreglo.
const showarray = () => [1, 2, 3];

// Ejecuté cada función para ver qué retorna.
console.log(showtext2());
console.log(shownumber());
console.log(showboolean());
console.log(showarray());
